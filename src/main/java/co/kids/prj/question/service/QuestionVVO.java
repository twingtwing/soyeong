package co.kids.prj.question.service;

public class QuestionVVO {
	private int qNo;
	private String Id;
	private String qTitle;
	private String qAnswer;
	private String qEmail;
	private String qFile;
	private String qPFile;
	private String qCategory;
	private String qProcess;

	public int getqNo() {
		return qNo;
	}

	public void setqNo(int qNo) {
		this.qNo = qNo;
	}

	public String getId() {
		return Id;
	}

	public void setId(String id) {
		Id = id;
	}

	public String getqTitle() {
		return qTitle;
	}

	public void setqTitle(String qTitle) {
		this.qTitle = qTitle;
	}

	public String getqAnswer() {
		return qAnswer;
	}

	public void setqAnswer(String qAnswer) {
		this.qAnswer = qAnswer;
	}

	public String getqEmail() {
		return qEmail;
	}

	public void setqEmail(String qEmail) {
		this.qEmail = qEmail;
	}

	public String getqFile() {
		return qFile;
	}

	public void setqFile(String qFile) {
		this.qFile = qFile;
	}

	public String getqPFile() {
		return qPFile;
	}

	public void setqPFile(String qPFile) {
		this.qPFile = qPFile;
	}

	public String getqCategory() {
		return qCategory;
	}

	public void setqCategory(String qCategory) {
		this.qCategory = qCategory;
	}

	public String getqProcess() {
		return qProcess;
	}

	public void setqProcess(String qProcess) {
		this.qProcess = qProcess;
	}
	
}
