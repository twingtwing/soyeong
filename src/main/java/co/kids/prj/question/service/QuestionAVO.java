package co.kids.prj.question.service;

public class QuestionAVO {
	private int fNo;
	private String fTitle;
	private String fAnswer;
	private String fCategorey;
	
	public int getfNo() {
		return fNo;
	}
	
	public void setfNo(int fNo) {
		this.fNo = fNo;
	}
	
	public String getfTitle() {
		return fTitle;
	}
	
	public void setfTitle(String fTitle) {
		this.fTitle = fTitle;
	}
	
	public String getfAnswer() {
		return fAnswer;
	}
	
	public void setfAnswer(String fAnswer) {
		this.fAnswer = fAnswer;
	}
	
	public String getfCategorey() {
		return fCategorey;
	}
	
	public void setfCategorey(String fCategorey) {
		this.fCategorey = fCategorey;
	}
	
}
